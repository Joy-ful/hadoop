package com.hadoop.bean1;

public class beann {
     Asset asset;
     BoundingVolume boundingVolume;
     Children children;
     Content content;
     JsonRootBean jsonRootBean;
     Root1 root;
     private Double geometricError;

    public Double getGeometricError() {
        return geometricError;
    }

    public void setGeometricError(Double geometricError) {
        this.geometricError = geometricError;
    }

    public Asset getAsset() {
        return asset;
    }

    public void setAsset(Asset asset) {
        this.asset = asset;
    }

    public BoundingVolume getBoundingVolume() {
        return boundingVolume;
    }

    public void setBoundingVolume(BoundingVolume boundingVolume) {
        this.boundingVolume = boundingVolume;
    }

    public Children getChildren() {
        return children;
    }

    public void setChildren(Children children) {
        this.children = children;
    }

    public Content getContent() {
        return content;
    }

    public void setContent(Content content) {
        this.content = content;
    }

    public JsonRootBean getJsonRootBean() {
        return jsonRootBean;
    }

    public void setJsonRootBean(JsonRootBean jsonRootBean) {
        this.jsonRootBean = jsonRootBean;
    }

    public Root1 getRoot() {
        return root;
    }

    public void setRoot(Root1 root) {
        this.root = root;
    }
}
